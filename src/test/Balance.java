    private void tableShow() {
        //dbConnection = getConnection();
        PreparedStatement pstmt = null;
        ResultSet rs = null;
        String balance = "SELECT p.name Имя,p.provider Поставщик,(IF(t2.number IS NOT NULL,t2.number,0) - IF(t1.number IS NOT NULL,t1.number,0)) Количество, p.price Цена\n"
                + "FROM \n"
                + " production p\n"
                + "LEFT JOIN \n"
                + " (SELECT p.name ,SUM(c.number) number\n"
                + "  FROM production p\n"
                + "  LEFT JOIN consumption c ON p.name = c.name\n"
                + "  GROUP BY p.name) t1 ON t1.name = p.name\n"
                + "LEFT JOIN \n"
                + " (SELECT p.name ,SUM(s.number) number\n"
                + "  FROM production p\n"
                + "  LEFT JOIN storage s ON p.name = s.name\n"
                + "  GROUP BY p.name) t2 ON p.name = t2.name";
        DefaultTableModel dtm = null;

        try {
            pstmt = dbConnection.prepareStatement(balance);
            dtm = new DefaultTableModel();

            if (pstmt.execute()) {
                rs = pstmt.getResultSet();

                //Формируем заголовки столбцов из названия полей -->
                ResultSetMetaData rsmd = rs.getMetaData();

                for (int col = 1; col <= rsmd.getColumnCount(); col++) {
                    dtm.addColumn(rsmd.getColumnLabel(col));

                }
                // <--
                while (rs.next()) {
                    Vector<String> row = new Vector<String>(); //Строка таблицы
                    /*Снова с помощью метаданных узнаем количество столбцов 
                     * в результате запроса и ниже в switch'е в зависимости 
                     * от типа текущего столбца забираем данные из ResultSet`a и пишем в 
                     * текущую строку - row. 
                     * */
                    for (int col = 1; col <= rsmd.getColumnCount(); col++) {
                        int type = rsmd.getColumnType(col);
                        switch (type) {

                            case Types.INTEGER:
                                row.add(Integer.toString(rs.getInt(col)));
                                break;

                            case Types.VARCHAR:
                                row.add(rs.getString(col));
                                break;

                            case Types.DECIMAL:
                                row.add(Double.toString(rs.getDouble(col)));
                                break;
                            default:
                                /*В этой моей тестовой таблице всего два типа полей: целое и строка. 
                                 *Соответственно если в таблице/запросе типов больше, то этот switch 
                                 *нужно расширить соответствующими типами, а сейчас, в случае если мы 
                                 *встретим неизвестный коду тип поля, я для наглядности бросаю исключение.                                    
                                 * */
                                throw new Exception("Неподдерживаемый тип");
                        }

                    }

                    dtm.addRow(row);
                }
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());

        } catch (Exception e) {
            e.printStackTrace();
        }
        jTable1.setModel(dtm);
    }

